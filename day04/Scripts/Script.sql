-- CASE 문
-- 값을 대체할 때 사용하는 문장
-- SELECT 절에서 사용된다.

SELECT
	CASE USER_PASSWORD
		WHEN '1234' THEN '일이삼사'
		WHEN '4567' THEN '사오육칠'
		ELSE '다른거'
	END
FROM TBL_USER;


-- 생일이 1990년도에 태어났다면 1990년도로 출력
-- 생일이 2000년도에 태어났다면 2000년도로 출력
SELECT 
	CASE
		WHEN USER_BIRTH >= TO_DATE('2000-01-01', 'YYYY-MM-DD') THEN '2000년생'
		WHEN USER_BIRTH <= TO_DATE('2000-01-01', 'YYYY-MM-DD') THEN '1990년생'
	END
FROM TBL_USER;

-- CASE문
-- 경기도에 살고 있는 사용자라면 경기도인
-- 서울에 살고 있는 사용자라면 서울인으로 출력

SELECT 
	CASE 
		WHEN USER_ADDRESS LIKE '%경기도%' THEN '경기도인'
		WHEN USER_ADDRESS LIKE '%서울시%' THEN '서울인'
	END
FROM TBL_USER;	

