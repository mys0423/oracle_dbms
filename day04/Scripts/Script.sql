-- 평균 댓글 개수보다 많이 달린 게시글
SELECT *
FROM TBL_POST
WHERE ID IN(
	SELECT POST_ID
	FROM TBL_REPLY 
	GROUP BY POST_ID
	HAVING COUNT(POST_ID) > (
		SELECT ROUND(AVG(REPLY_COUNT), 1)
		FROM (
			SELECT COUNT(ID) AS REPLY_COUNT
			FROM TBL_REPLY
			GROUP BY POST_ID
		)
	)
);

