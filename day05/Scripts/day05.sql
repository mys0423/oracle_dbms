/*
 * GROUP BY부터 생각을 많이 해야한다.
 * FROM -> WHERE -> GROUP BY -> HAVING -> SELECT > ORDER BY
 * 
*/


SELECT * FROM TBL_REPLY;
SELECT * FROM TBL_POST;
SELECT * FROM TBL_USER;

SELECT TBP.*, TBU.USER_EMAIL  
FROM TBL_POST TBP
JOIN TBL_USER TBU 
ON TBP.USER_ID = TBU.ID;

SELECT TBR.REPLY_CONTENT, TBP.POST_TITLE  
FROM TBL_REPLY TBR 
JOIN TBL_POST TBP 
ON TBR.POST_ID = TBP.ID;


-- 댓글 테이블, 유저 테이블 조인 후
-- 댓글의 작성자와 댓글 내용을 조회
SELECT TBR.REPLY_CONTENT, TBU.USER_EMAIL 
FROM TBL_REPLY TBR
JOIN TBL_USER TBU 
ON TBR.USER_ID = TBU.ID;

-- 3개
-- 게시글 제목, 댓글 내용, 작성자
SELECT DISTINCT TBP.POST_TITLE
FROM TBL_REPLY TBR
JOIN TBL_USER TBU 
ON TBR.USER_ID = TBU.ID 
JOIN TBL_POST TBP 
ON TBR.POST_ID = TBP.ID
WHERE POST_TITLE LIKE '%유중의 미%';


-- 댓글이 2개 이상 달린 게시글의 작성자 이메일, 주소를 조회하기
SELECT DISTINCT TBU.USER_EMAIL, TBU.USER_ADDRESS 
FROM TBL_REPLY TBR
JOIN TBL_USER TBU 
ON TBR.USER_ID = TBU.ID 
JOIN TBL_POST TBP 
ON TBR.POST_ID = TBP.ID
WHERE TBP.ID IN (
	SELECT POST_ID 
	FROM TBL_REPLY
	GROUP BY POST_ID 
	HAVING COUNT(REPLY_CONTENT) >= 2 
);

