-- 내용에 뀨트북이 들어간 게시글의 달린 댓글 조회
SELECT * 
FROM TBL_REPLY
WHERE POST_ID IN (
	SELECT ID
	FROM TBL_POST
	WHERE POST_CONTENT LIKE '%뀨트북%'
);

-- 내용에 포도가 들어간 글을 작성한 작성자의 이메일 조회
SELECT USER_EMAIL 
FROM TBL_USER 
WHERE ID IN (
	SELECT USER_ID
	FROM TBL_POST
	WHERE POST_CONTENT LIKE '%포도%'
);

-- 2000년생 유저가 작성한 댓글 조회
SELECT REPLY_CONTENT 
FROM TBL_REPLY TBR
JOIN TBL_USER TBU 
ON TBR.USER_ID = TBU.ID 
WHERE TBU.USER_BIRTH >= '2000-01-01' AND TBU.USER_BIRTH <= '2000-12-31';

-- 가장 나이가 어린 사용자가 작성한 게시글 조회
SELECT POST_CONTENT 
FROM TBL_POST TBP
JOIN TBL_USER TBU 
ON TBP.USER_ID = TBU.ID 
WHERE TBU.USER_BIRTH = (
	SELECT MAX(USER_BIRTH)
	FROM TBL_USER 
); 

-- 주소가 '구'로 끝나는 유저가 작성한 게시글에 달린 모든 댓글 조회
SELECT TBR.REPLY_CONTENT 
FROM TBL_REPLY TBR
JOIN TBL_POST TBP 
ON TBR.POST_ID = TBP.ID 
JOIN TBL_USER TBU
ON TBP.USER_ID = TBU.ID 
WHERE TBU.USER_ADDRESS LIKE '%구';

